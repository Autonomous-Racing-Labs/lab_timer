/*--------------------------------------------------------------------------------------
  Includes
--------------------------------------------------------------------------------------*/
#include <Arduino.h>
#include <lap_display.h>

lap_display dmd;

/*--------------------------------------------------------------------------------------
  setup
  Called by the Arduino architecture before the main loop begins
--------------------------------------------------------------------------------------*/
void setup(void)
{
  Serial.begin(9600);
  dmd.reset_timer();

}

/*--------------------------------------------------------------------------------------
  loop
  Arduino architecture main loop
--------------------------------------------------------------------------------------*/
void loop(void)
{
  dmd.start_timer();
  delay(2000);
  dmd.lap();
  delay(5000);
  dmd.reset_timer();
  delay(2000);
}